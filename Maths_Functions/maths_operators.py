# Matha Operators

print(2 + 3)    # 4
print(5 - 3)    # 2
print( 4 * 3)   # 12
print(7 / 2)    # 3.5

# ( // ) Floor Division -->  It divides two numbers and rounds down
print(7 // 2)   # 3

# ( % ) remainder  -->  the leftover part after division - used to check if a number is even
print( 7 % 2)   # 1

# ( ** ) exponentiation -->  it raises a number to the power of another number 
print(2 ** 3)   # 8